count([], 0).

count([_|T], N) :- count(T, N1), N is N1 + 1.

sum([], 0).

sum([H|T], S) :- sum(T, S1), S is S1 + H.



% ====================
% ======QUERY=========
% ====================


% count([1, 2, 4, 1, 2])
% N = 5

% count([2, 4, 1, 7, 4, 5, 9], N)
% N = 7


% sum([2, 4, 1, 7, 4, 5, 9], N)
% N = 32